public class FirstIfs{
    public static void main(String[] args) {
        int num = 3;

        if (num==1) {
            System.out.println(num+" == 1");
        }
        if (num!=1) {
            System.out.println(num+" != 1");
        }
        if (num>1) {
            System.out.println(num+" > 1");
        }
        if (num<1) {
            System.out.println(num+" < 1");
        }
    }
}